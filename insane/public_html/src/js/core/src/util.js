'use strict';

define([], function () {
    return {
        //TODO : later we shall move to LESS
        loadStyle: function (url) {
            var link = document.createElement("link");
            link.type = "text/css";
            link.rel = "stylesheet";
            link.href = url;
            document.getElementsByTagName("head")[0].appendChild(link);
        }
    };
});
